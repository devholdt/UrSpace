/**
 *
 * @param {number} a This is the first value
 * @param {number} b This is the second value
 * @returns {number} The sum of value a and value b
 * @example
 * // Add a number value to params a and b
 * ```js
 * addNumbers(4, 7);
 * ```
 * // Returns number 11
 */
function addNumbers(a, b) {
  const result = a + b;
  return result;
}
